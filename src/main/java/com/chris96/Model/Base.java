package com.chris96.Model;

public class Base {
    private final int WIDTH;
    private final int HEIGHT;

    public int getWIDTH() {
        return WIDTH;
    }

    public int getHEIGHT() {
        return HEIGHT;
    }

    public Base(final int width, final int height) {
        this.WIDTH = width;
        this.HEIGHT = height;
    }
}
